package com.example.foodrecipes.model;

public class tintuc {
    private int image;
    private String mota;

    public tintuc(int image, String mota) {
        this.image = image;
        this.mota = mota;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getMota() {
        return mota;
    }

    public void setMota(String mota) {
        this.mota = mota;
    }
}
